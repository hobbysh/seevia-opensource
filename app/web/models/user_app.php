class UserApp extends AppModel
{
    /*
     * @var $useDbConfig 数据库配置
     */
    public $useDbConfig = 'oms';
    public $name = 'UserApp';

    /**
     * 函数app_status 分享状态.
     */
    public function app_status()
    {
        $app_share = $this->find('count', array('conditions' => array('status' => 1)));

        return $app_share;
    }
}
